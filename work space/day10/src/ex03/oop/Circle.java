package ex03.oop;
//알트+쉬프트+s
//멤버변수의 개수는 알려줘야합니다.
public class Circle {
	int x,y,z;

	public int getX() {		return x;	}
	public int getY() {		return y;	}
	public int getZ() {		return z;	}

	public void setX(int x) {		this.x = x;}		
	public void setY(int y) {		this.y = y;	}
	public void setZ(int z) {		this.z = z;	}
	
}
