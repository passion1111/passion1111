class Ex01_print {

	public static void main(String[] args) {
		
	   System.out.print('A'); // ; 문장종결기호
  	   System.out.print("\n");  //제어문자
	   System.out.println("\n\nstring\t\t kbs mbc");
	   System.out.printf("%d %c\n",100,'b');
 	   System.out.printf("\n%f %s\n",12.34,"kbs");
  	   System.out.printf("%.1f %s",12.34,"kbs");
	}

} // end class